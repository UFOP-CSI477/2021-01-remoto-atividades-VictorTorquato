<label for="funcaoText" style="font-weight: bold">Função do componente:</label>
<p id="funcaoText">Todos os sistemas de rotação do veículo dependem delas. São quatro tipos: dentada, do alternador, do ar e da direção hidráulica. São responsáveis por transmitir a força e os movimentos de um componente para outra peça.</p>

<label for="manutencaoText" style="font-weight: bold">Manutenção do componente:</label>
<p id="manutencaoText">Com o motor frio, deve-se verificar a qualidade das correias, sinais de desgaste, presença de fluidos, rachaduras, falta de aderência e falta ou excesso de tensão podem gerar diversos tipos de problemas na performance do carro. Para verificar a tensão, deve se pressionar a correia no meio do seu lado maior, a tensão deve garantir que ela não se mova mais que dois centímetros.</p>

<p>Qualquer sinal de desgaste indica que a correia precisa ser trocada, os componentes que ela faz conexão também devem ser verificados.

<label for="manutencaoText" style="font-weight: bold; margin-top: 1rem">Problemas causados pela falta de manutenção:</label>
<p>A falta de manutenção de tais correias gera perdas no desempenho do automóvel e danificar outros componentes. Caso a correia dentada quebre por exemplo, o motor para de funcionar, pode empenar válvulas e causar danos aos pistões do motor.</p>


